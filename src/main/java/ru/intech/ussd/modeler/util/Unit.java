package ru.intech.ussd.modeler.util;

public class Unit<T> {
	// =================================================================================================================
	// Constants
	// =================================================================================================================

	// =================================================================================================================
	// Fields
	// =================================================================================================================
	private T val;

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public Unit() {
		setVal(null);
	}

	public Unit(T val) {
		setVal(val);
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================
	public T getVal() {
		return val;
	}

	public void setVal(T val) {
		this.val = val;
	}

	// =================================================================================================================
	// Methods
	// =================================================================================================================

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}

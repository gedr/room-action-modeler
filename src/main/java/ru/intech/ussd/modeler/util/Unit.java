package ru.intech.ussd.modeler.util;

public class Unit<T> {
	// =================================================================================================================
	// Constants
	// =================================================================================================================

	// =================================================================================================================
	// Fields
	// =================================================================================================================
	private T value;

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public Unit() {
		setValue(null);
	}

	public Unit(T val) {
		setValue(val);
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================
	public T getValue() {
		return value;
	}

	public void setValue(T val) {
		this.value = val;
	}

	// =================================================================================================================
	// Methods
	// =================================================================================================================

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}

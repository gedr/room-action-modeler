package ru.intech.ussd.modeler.events;

import org.apache.commons.lang3.Validate;

import ru.intech.ussd.modeler.graphobjects.Edge;

public class EdgeEvent {
	// =================================================================================================================
	// Constants
	// =================================================================================================================

	// =================================================================================================================
	// Fields
	// =================================================================================================================
	private EventType eventType;
	private Edge edge;

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public EdgeEvent(Edge edge, EventType eventType) {
		Validate.notNull(edge);
		Validate.notNull(eventType);
		this.edge = edge;
		this.eventType = eventType;
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================
	public Edge getEdge() {
		return edge;
	}

	public EventType getEventType() {
		return eventType;
	}

	// =================================================================================================================
	// Methods
	// =================================================================================================================

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================

}

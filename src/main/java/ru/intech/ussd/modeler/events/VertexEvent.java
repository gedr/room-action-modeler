package ru.intech.ussd.modeler.events;

import org.apache.commons.lang3.Validate;

import ru.intech.ussd.modeler.graphobjects.Vertex;

public class VertexEvent implements GraphEvent {
	// =================================================================================================================
	// Constants
	// =================================================================================================================

	// =================================================================================================================
	// Fields
	// =================================================================================================================
	private EventType eventType;
	private Vertex vertex;

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public VertexEvent(Vertex vertex, EventType eventType) {
		Validate.notNull(vertex);
		Validate.notNull(eventType);
		this.vertex = vertex;
		this.eventType = eventType;
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================
	public Vertex getVertex() {
		return vertex;
	}

	public EventType getEventType() {
		return eventType;
	}

	// =================================================================================================================
	// Methods
	// =================================================================================================================

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}
